    private void bNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bNextActionPerformed
        Calendar date = Calendar.getInstance();
        if(properData())
        {
            date.set(Calendar.YEAR, cbYear.getSelectedIndex() + 1899);
            date.set(Calendar.MONTH, cbMonth.getSelectedIndex() - 1);
            date.set(Calendar.DAY_OF_MONTH, cbDay.getSelectedIndex());
            getMultimedia().setName(tName.getText());
            getMultimedia().setCountry(tCountry.getText());
            getMultimedia().setDescription(tDescription.getText());

            getMultimedia().setDistributor(getDb().findDist(cbDistributor.getSelectedItem().toString()));
            getMultimedia().setDateProduction(date);
            getMultimedia().setDuration((int) sTime.getValue());
            getMultimedia().setRating((int) sRating.getValue());
            if(getMultimedia() instanceof Movie){
                swapPanels(new AddMovieControlPanel(getMultimedia()));
            }
            if(getMultimedia() instanceof Series){
                swapPanels(new AddSerieControlPanel(getMultimedia()));
            }
            if(getMultimedia() instanceof LiveStream){
                swapPanels(new AddLSControlPanel(getMultimedia()));
            }
        }
    }//GEN-LAST:event_bNextActionPerformed


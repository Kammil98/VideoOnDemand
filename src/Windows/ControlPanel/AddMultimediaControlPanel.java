    /**
     * inserting text which was in Panel befor going Next Panel
     */
    private void setText(){
        Calendar date = getMultimedia().getDateProduction();
        tName.setText(getMultimedia().getName());
        tCountry.setText(getMultimedia().getCountry());
        cbDistributor.setSelectedIndex(getDb().findDistIndex(getMultimedia().getDistributor().getDistName()) + 1);
        cbDay.setSelectedIndex(date.get(Calendar.DAY_OF_MONTH));
        cbMonth.setSelectedIndex(date.get(Calendar.MONTH));
        cbYear.setSelectedIndex(date.get(Calendar.YEAR) - 1899);
        sTime.setValue(getMultimedia().getDuration());
        sRating.setValue(getMultimedia().getRating());
        tDescription.setText(getMultimedia().getDescription());
    }

    /**
     * Check if the Data have proper values and saving values in movie if it's proper or send notifications for user if it's not
     */
    private boolean takeData(){
        try{
            getLs().setPrice(Float.parseFloat(tPrice.getText()));
        }
        catch(NullPointerException | NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Prosze sprawdzic, czy wszystkie pola wymagane, \nzostaly wypelnione poprawnie.");
            return false;
        }
        return true;
    }
    private void bAddPromotionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddPromotionActionPerformed
        if(takeData()){
            swapPanels(new AddPromotionControlPanel(getLs()));
        }
    }//GEN-LAST:event_bAddPromotionActionPerformed


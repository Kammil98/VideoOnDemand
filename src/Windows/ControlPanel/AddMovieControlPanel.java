    private void bAddActorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddActorActionPerformed
        if(tActor.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Nie wpisano aktora, prosze wypelnic pole \"Aktor\".");
        }
        else{
            getMovie().setActor(tActor.getText());
            tActor.setText("");
        }
    }//GEN-LAST:event_bAddActorActionPerformed

    private void bAddLinkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddLinkActionPerformed
        if(tLink.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Nie wpisano linku, prosze wypelnic pole \"Link\".");
        }
        else{
            getMovie().setLink(tLink.getText());
            tLink.setText("");
        }
    }//GEN-LAST:event_bAddLinkActionPerformed

    private void bAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddActionPerformed
        if(takeData()){
            getDb().getMultimedias().add(getMovie());
            swapPanels(new MainControlPanel());
        }
        
    }//GEN-LAST:event_bAddActionPerformed

    private void bAddPromotionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddPromotionActionPerformed
        if(takeData()){
            swapPanels(new AddPromotionControlPanel(getMovie()));
        }
    }//GEN-LAST:event_bAddPromotionActionPerformed



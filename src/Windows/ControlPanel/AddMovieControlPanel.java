    private void swapPanels(JPanel panel){
        setVisible(false);
        getParent().add(panel);
        panel.setVisible(true);  
    }
    /**
     * Check if the Data have proper values and saving values in movie if it's proper or send notifications for user if it's not
     */
    private boolean takeData(){
        getMovie().setTimeToWatch((int) sTime.getValue());
        getMovie().setType((Type)cbType.getSelectedItem());
        if(!tActor.getText().equals(""))
            getMovie().setActor(tActor.getText());
        if(!tLink.getText().equals(""))
            getMovie().setActor(tLink.getText());
        try{
            getMovie().setPrice(Float.parseFloat(tPrice.getText()));
        }
        catch(NullPointerException | NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Proszę sprawdzić, czy wszystkie wymagane pola, \nzostały wypełnione poprawnie.");
            return false;
        }
        return true;
    }
